
parcels = []

n = int(input("Скільки бандеролей ввести? "))

for i in range(n):
    print(f"\n=== Бандероль {i + 1} ===")
    parcel = {
        "місто_призначення": input("Місто призначення: "),
        "прізвище_отримувача": input("Прізвище отримувача: "),
        "місяць_відправлення": input("Місяць відправлення: "),
        "дата_відправлення": int(input("Дата відправлення (1-31): ")),
        "вага_гр": float(input("Вага бандеролі (гр.): ")),
        "цінна": input("Бандероль є цінною? (так/ні): ")
    }
    parcels.append(parcel)

print("\n====== СПИСОК БАНДЕРОЛЕЙ ======")
for i, p in enumerate(parcels):
    print(f"\nБандероль #{i + 1}:")
    print(f"  Місто призначення:   {p['місто_призначення']}")
    print(f"  Отримувач:           {p['прізвище_отримувача']}")
    print(f"  Місяць відправлення: {p['місяць_відправлення']}")
    print(f"  Дата відправлення:   {p['дата_відправлення']}")
    print(f"  Вага:                {p['вага_гр']} гр.")
    print(f"  Цінна:               {p['цінна']}")